class Numbers {
    public static void main(String[] args){
        int a = 20;
        int b= -8;
        if (a>=0){
            System.out.println(a + " is positive number");
        }
        else {
            System.out.println(a +" is negative number");
        }
        if (b>=0){
            System.out.println(b + " is positive number");
        }
        else {
            System.out.println(b+" is negative number");
        }
    }
}
